package ders04_matematikselislemler;

public class C06_pre_postincrement {
    public static void main(String[] args) {

        int a=10;
        // a vereablenin degerini yazdirip sonra a nin degerini bir artirin
        System.out.println("a :"+a);
        a++;

       int b=10;
        // b vereablesinin bir artirip sonra yazdirin

        b++;
        System.out.println("b :"+b);

        int c=10;
        // c vereablenin degerini yazdirip  bir sonra artir
        System.out.println("c :"+ c++);

        int d=10;
        // d variable nin degerini bir arttirip sonra yazdirin
        System.out.println("d:"+ ++d);

    }
}
